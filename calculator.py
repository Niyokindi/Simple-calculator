def add(a, b):
    sum = a+b  # Adding two numbers and assigning the result to a variable "sum"
    print(sum)  # Displays the sum to the console
    return sum  # Returning the sum of the two numbers


def subtract(a, b):
    difference = a - b
    print(difference)  # Displays the difference to the console
    return difference


add(6, 5)

subtract(6, 5)
